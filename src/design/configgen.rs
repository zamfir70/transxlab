/// Config generation: emit ready-to-use training configs for HF Trainer, Axolotl, LLaMA-Factory.
use crate::design::architecture::ArchitectureSpec;
use crate::design::interview::{DesignInputs, TrainingApproach};

/// Emit a HuggingFace TrainingArguments-compatible YAML config.
pub fn generate_hf_trainer_config(inputs: &DesignInputs, spec: &ArchitectureSpec) -> String {
    let mut lines = Vec::new();
    lines.push("# HuggingFace Trainer config".to_string());
    lines.push("# Generated by TransXLab".to_string());
    lines.push(format!(
        "# Task: {}",
        if inputs.task_description.is_empty() {
            inputs.task_type.as_str()
        } else {
            &inputs.task_description
        }
    ));
    lines.push(String::new());

    // Model
    if inputs.approach == TrainingApproach::FineTune && !spec.base_model.is_empty() {
        lines.push(format!("model_name_or_path: \"{}\"", spec.base_model));
    }

    lines.push(String::new());
    lines.push("# Training arguments".to_string());

    if let Some(ref sched) = spec.schedule {
        lines.push(format!("output_dir: \"./output\""));
        lines.push(format!("num_train_epochs: {}", sched.epochs));
        lines.push(format!("per_device_train_batch_size: {}", sched.batch_size));
        lines.push(format!(
            "per_device_eval_batch_size: {}",
            sched.batch_size
        ));
        lines.push(format!(
            "gradient_accumulation_steps: {}",
            sched.grad_accum_steps
        ));
        lines.push(format!("learning_rate: {:.1e}", sched.lr));
        lines.push(format!("weight_decay: {}", sched.weight_decay));
        lines.push(format!("warmup_steps: {}", sched.warmup_steps));
        lines.push(format!("lr_scheduler_type: \"cosine\""));

        let use_bf16 = sched.precision == "bf16";
        lines.push(format!("bf16: {}", use_bf16));
        lines.push(format!("fp16: {}", !use_bf16));

        if sched.label_smoothing > 0.0 {
            lines.push(format!("label_smoothing_factor: {}", sched.label_smoothing));
        }

        lines.push(format!("max_grad_norm: 1.0"));
        lines.push(format!("logging_steps: 10"));
        lines.push(format!("eval_strategy: \"steps\""));
        lines.push(format!("eval_steps: 500"));
        lines.push(format!("save_strategy: \"steps\""));
        lines.push(format!("save_steps: 500"));
        lines.push(format!("save_total_limit: 3"));
        lines.push(format!("load_best_model_at_end: true"));
        lines.push(format!("report_to: \"none\""));
    }

    if sched_is_lora(spec) {
        lines.push(String::new());
        lines.push("# Note: LoRA/QLoRA requires PEFT wrapper — see peft_config below".to_string());
    }

    lines.join("\n")
}

/// Emit a PEFT LoRA config (used alongside HF Trainer).
pub fn generate_peft_config(spec: &ArchitectureSpec) -> Option<String> {
    let lora = spec.lora_config.as_ref()?;
    let mut lines = Vec::new();
    lines.push("# PEFT LoRA config".to_string());
    lines.push("# Generated by TransXLab".to_string());
    lines.push(String::new());
    lines.push(format!("r: {}", lora.r));
    lines.push(format!("lora_alpha: {}", lora.alpha));
    lines.push(format!("lora_dropout: {}", lora.dropout));
    lines.push(format!(
        "target_modules:\n{}",
        lora.target_modules
            .iter()
            .map(|m| format!("  - \"{}\"", m))
            .collect::<Vec<_>>()
            .join("\n")
    ));
    lines.push(format!("bias: \"none\""));
    lines.push(format!("task_type: \"CAUSAL_LM\""));

    if spec.training_method == "qlora" {
        lines.push(String::new());
        lines.push("# QLoRA quantization".to_string());
        lines.push("quantization:".to_string());
        lines.push("  load_in_4bit: true".to_string());
        lines.push("  bnb_4bit_compute_dtype: \"bfloat16\"".to_string());
        lines.push("  bnb_4bit_quant_type: \"nf4\"".to_string());
        lines.push("  bnb_4bit_use_double_quant: true".to_string());
    }

    Some(lines.join("\n"))
}

/// Emit an Axolotl-compatible YAML config.
pub fn generate_axolotl_config(inputs: &DesignInputs, spec: &ArchitectureSpec) -> String {
    let mut lines = Vec::new();
    lines.push("# Axolotl config".to_string());
    lines.push("# Generated by TransXLab".to_string());
    lines.push(String::new());

    if inputs.approach == TrainingApproach::FineTune && !spec.base_model.is_empty() {
        lines.push(format!("base_model: \"{}\"", spec.base_model));
        lines.push(format!("model_type: \"AutoModelForCausalLM\""));
    }

    lines.push(format!("tokenizer_type: \"AutoTokenizer\""));
    lines.push(String::new());

    // LoRA
    if let Some(ref lora) = spec.lora_config {
        lines.push(format!("adapter: \"lora\""));
        lines.push(format!("lora_r: {}", lora.r));
        lines.push(format!("lora_alpha: {}", lora.alpha));
        lines.push(format!("lora_dropout: {}", lora.dropout));
        lines.push(format!(
            "lora_target_modules:\n{}",
            lora.target_modules
                .iter()
                .map(|m| format!("  - \"{}\"", m))
                .collect::<Vec<_>>()
                .join("\n")
        ));
        if spec.training_method == "qlora" {
            lines.push(format!("load_in_4bit: true"));
        }
        lines.push(String::new());
    }

    // Data
    lines.push("datasets:".to_string());
    lines.push("  - path: \"./data/train.jsonl\"".to_string());
    lines.push("    type: \"completion\"".to_string());
    lines.push(String::new());

    lines.push(format!("val_set_size: 0.1"));
    lines.push(format!("output_dir: \"./output\""));
    lines.push(String::new());

    // Training
    if let Some(ref sched) = spec.schedule {
        lines.push(format!("num_epochs: {}", sched.epochs));
        lines.push(format!(
            "micro_batch_size: {}",
            sched.batch_size
        ));
        lines.push(format!(
            "gradient_accumulation_steps: {}",
            sched.grad_accum_steps
        ));
        lines.push(format!("learning_rate: {:.1e}", sched.lr));
        lines.push(format!("weight_decay: {}", sched.weight_decay));
        lines.push(format!("warmup_steps: {}", sched.warmup_steps));
        lines.push(format!("lr_scheduler: \"cosine\""));
        lines.push(format!("bf16: true"));
        lines.push(format!("max_grad_norm: 1.0"));
        lines.push(String::new());
        lines.push(format!("logging_steps: 10"));
        lines.push(format!("eval_steps: 500"));
        lines.push(format!("save_steps: 500"));
        lines.push(format!("save_total_limit: 3"));
    }

    lines.join("\n")
}

/// Emit a LLaMA-Factory compatible YAML config.
pub fn generate_llamafactory_config(inputs: &DesignInputs, spec: &ArchitectureSpec) -> String {
    let mut lines = Vec::new();
    lines.push("# LLaMA-Factory config".to_string());
    lines.push("# Generated by TransXLab".to_string());
    lines.push(String::new());

    lines.push("### model".to_string());
    if inputs.approach == TrainingApproach::FineTune && !spec.base_model.is_empty() {
        lines.push(format!("model_name_or_path: \"{}\"", spec.base_model));
    }
    lines.push(String::new());

    lines.push("### method".to_string());
    lines.push(format!("stage: \"sft\""));
    lines.push(format!("do_train: true"));

    if let Some(ref lora) = spec.lora_config {
        lines.push(format!("finetuning_type: \"lora\""));
        lines.push(format!("lora_rank: {}", lora.r));
        lines.push(format!("lora_alpha: {}", lora.alpha));
        lines.push(format!("lora_dropout: {}", lora.dropout));
        lines.push(format!(
            "lora_target: \"{}\"",
            lora.target_modules.join(",")
        ));
        if spec.training_method == "qlora" {
            lines.push(format!("quantization_bit: 4"));
        }
    } else {
        lines.push(format!("finetuning_type: \"full\""));
    }
    lines.push(String::new());

    lines.push("### dataset".to_string());
    lines.push(format!("dataset: \"custom_train\""));
    lines.push(format!("template: \"default\""));
    lines.push(String::new());

    lines.push("### output".to_string());
    lines.push(format!("output_dir: \"./output\""));
    lines.push(String::new());

    lines.push("### train".to_string());
    if let Some(ref sched) = spec.schedule {
        lines.push(format!("num_train_epochs: {:.1}", sched.epochs as f64));
        lines.push(format!(
            "per_device_train_batch_size: {}",
            sched.batch_size
        ));
        lines.push(format!(
            "gradient_accumulation_steps: {}",
            sched.grad_accum_steps
        ));
        lines.push(format!("learning_rate: {:.1e}", sched.lr));
        lines.push(format!("weight_decay: {}", sched.weight_decay));
        lines.push(format!("warmup_steps: {}", sched.warmup_steps));
        lines.push(format!("lr_scheduler_type: \"cosine\""));
        lines.push(format!("bf16: true"));
        lines.push(format!("max_grad_norm: 1.0"));
        lines.push(String::new());
        lines.push(format!("logging_steps: 10"));
        lines.push(format!("save_steps: 500"));
        lines.push(format!("save_total_limit: 3"));
    }

    lines.join("\n")
}

fn sched_is_lora(spec: &ArchitectureSpec) -> bool {
    spec.training_method == "lora" || spec.training_method == "qlora"
}

/// Save all config variants to the output directory.
pub fn save_configs(
    output_dir: &std::path::Path,
    inputs: &DesignInputs,
    spec: &ArchitectureSpec,
) -> std::io::Result<()> {
    let configs_dir = output_dir.join("configs");
    std::fs::create_dir_all(&configs_dir)?;

    std::fs::write(
        configs_dir.join("hf_trainer.yaml"),
        generate_hf_trainer_config(inputs, spec),
    )?;

    if let Some(peft) = generate_peft_config(spec) {
        std::fs::write(configs_dir.join("peft_lora.yaml"), peft)?;
    }

    std::fs::write(
        configs_dir.join("axolotl.yaml"),
        generate_axolotl_config(inputs, spec),
    )?;

    std::fs::write(
        configs_dir.join("llamafactory.yaml"),
        generate_llamafactory_config(inputs, spec),
    )?;

    Ok(())
}
